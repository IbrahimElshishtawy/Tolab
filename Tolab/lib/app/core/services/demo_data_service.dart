  List<StaffMetricPoint> _metricTrend(List<double> values) {
    const labels = ['Mon', 'Tue', 'Wed', 'Thu', 'Fri', 'Sat', 'Sun'];
    return List<StaffMetricPoint>.generate(
      values.length,
      (index) => StaffMetricPoint(
        label: labels[index.clamp(0, labels.length - 1)],
        value: values[index],
      ),
    );
  }
