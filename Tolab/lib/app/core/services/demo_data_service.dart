  StaffAdminRecord _staffRecord({
    required String id,
    required String fullName,
    required String email,
    required String employeeId,
    required String role,
    required String? doctorType,
    required String department,
    required String status,
    required String accountCreationStatus,
    required String roleSummary,
    required String recentActivity,
    required String createdAtLabel,
    required String lastActiveLabel,
    required double attendanceRate,
    required double engagementRate,
    required int lecturesUploaded,
    required int tasksCreated,
    required int postsCreated,
    required int uploadsCount,
    required int scheduleUpdates,
    required List<StaffSubjectAssignment> subjects,
    required List<StaffPermissionGroup> permissionGroups,
    required List<StaffTimelineEvent> timeline,
    required List<StaffMetricPoint> attendanceTrend,
    required List<StaffMetricPoint> engagementTrend,
  }) {
    final totalPermissions = permissionGroups.fold<int>(
      0,
      (sum, group) => sum + group.permissions.length,
    );
    final enabledPermissions = permissionGroups.fold<int>(
      0,
      (sum, group) =>
          sum +
          group.permissions.where((permission) => permission.enabled).length,
    );

    return StaffAdminRecord(
      id: id,
      fullName: fullName,
      email: email,
      employeeId: employeeId,
      role: role,
      doctorType: doctorType,
      department: department,
      status: status,
      accountCreationStatus: accountCreationStatus,
      roleSummary: roleSummary,
      recentActivity: recentActivity,
      createdAtLabel: createdAtLabel,
      lastActiveLabel: lastActiveLabel,
      attendanceRate: attendanceRate,
      engagementRate: engagementRate,
      permissionCoverage: totalPermissions == 0
          ? 0
          : (enabledPermissions / totalPermissions) * 100,
      subjectsAssigned: subjects.length,
      lecturesUploaded: lecturesUploaded,
      tasksCreated: tasksCreated,
      postsCreated: postsCreated,
      uploadsCount: uploadsCount,
      scheduleUpdates: scheduleUpdates,
      subjects: subjects,
      permissionGroups: permissionGroups,
      timeline: timeline,
      attendanceTrend: attendanceTrend,
      engagementTrend: engagementTrend,
    );
  }

